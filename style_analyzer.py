from groq import Groq
from config import GROQ_API_KEY, EXAMPLE_POSTS

client = Groq(api_key=GROQ_API_KEY)


def analyze_channel_style() -> str:
    posts_text = "\n\n---\n\n".join(EXAMPLE_POSTS)

    response = client.chat.completions.create(
        model="llama-3.3-70b-versatile",
        messages=[
            {
                "role": "user",
                "content": f"""Проанализируй стиль этих постов из Telegram-канала про JDM машины.

Посты:
{posts_text}

Опиши подробно:
1. Тон и подачу (восторженный, технический, разговорный и т.д.)
2. Структуру поста (как начинается, как заканчивается)
3. Использование эмодзи (где и какие)
4. Стиль описания техническихбарактеристики
5. Использование хэштегов
(6. Длинупоство 
7. Особые фразы или обороты

Дай краткое rewюме стиля для дальнейшего использования при генерации похожихпостов."""
            }
        ],
        max_tokens=1000,
    )

    style = response.choices[0].message.content
    print("✅ Стиль канала проанализирован")
    return style


